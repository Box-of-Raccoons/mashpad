# mashpad/melodies.py — children's-song melodies + a sequential note sequencer.
#
# Pure — no pygame, no numpy, no I/O (joins the purity test). main.py pulls the
# next note name off a MelodySequencer on every allowed spawn; audio.py plays the
# matching sounds/notes/<name>.wav. Note names here are the WAV filenames.
#
# Songs are authored in a compact letter notation so each transcription reads
# like sheet music and can be checked by eye against docs/piano-melodies-plan.md:
#   * a letter A–G is a natural note in the base octave (C major, no accidentals)
#   * a trailing "'" raises it one octave, a trailing "," drops it one octave
#   * "|" and whitespace are bar/rhythm separators and are ignored
# _parse() turns that into the ("g4", "c5", …) filenames the audio layer wants.

from __future__ import annotations

from dataclasses import dataclass

# The diatonic C-major note set generated by gen_notes.py (G4–C6). Kept as a
# literal here so this module stays pure/importable without numpy; a drift test
# (tests/test_melodies.py) asserts it matches gen_notes.NOTE_NAMES exactly.
NOTE_RANGE: tuple[str, ...] = (
    "g4", "a4", "b4", "c5", "d5", "e5", "f5", "g5", "a5", "b5", "c6",
)
_VALID = frozenset(NOTE_RANGE)

# Octave an unmarked letter lands in; "," drops one, "'" raises one. The audition
# winner was the C5-root xylophone, so unmarked C is c5 (design table +12).
_BASE_OCTAVE = 5


def _parse(notation: str) -> tuple[str, ...]:
    """Compact letter notation → note filenames.

    'C' → 'c5',  "C'" → 'c6',  'G,' → 'g4'. Bars ('|') and whitespace separate
    notes and are ignored. Raises ValueError on an unrecognised token so a
    fat-fingered transcription fails loudly at import instead of playing silence.
    """
    notes: list[str] = []
    for token in notation.replace("|", " ").split():
        letter = token[0].lower()
        suffix = token[1:]
        if letter not in "abcdefg":
            raise ValueError(f"bad note letter in token {token!r}")
        octave = _BASE_OCTAVE
        if suffix == "'":
            octave += 1
        elif suffix == ",":
            octave -= 1
        elif suffix != "":
            raise ValueError(f"bad octave mark in token {token!r}")
        notes.append(f"{letter}{octave}")
    return tuple(notes)


@dataclass(frozen=True)
class Song:
    """A named melody as an ordered tuple of note filenames (no durations)."""
    name: str
    notes: tuple[str, ...]


def _song(name: str, notation: str) -> Song:
    return Song(name, _parse(notation))


