def _validate(songs: list[Song]) -> None:
    """Every song must be non-empty and use only generated (in-range) notes."""
    for song in songs:
        if not song.notes:
            raise ValueError(f"song {song.name!r} has no notes")
        bad = [n for n in song.notes if n not in _VALID]
        if bad:
            raise ValueError(f"song {song.name!r} uses out-of-range notes {bad}")


_validate(SONGS)


class MelodySequencer:
    """Walks the song list one note per next() call, looping forever.

    Sequential: each next() returns the current song's next note; at a song's end
    it advances to the following song (the last song wraps back to the first). No
    RNG — the order is deterministic. A dropped note upstream (all mixer channels
    busy) is the caller's problem; the sequence still advances by one per call,
    which is the intended "any key = next note" behaviour.
    """

    def __init__(self, songs: list[Song] | None = None) -> None:
        self._songs = songs if songs is not None else SONGS
        if not self._songs:
            raise ValueError("MelodySequencer needs at least one song")
        self._song_idx = 0
        self._note_idx = 0

    def next(self) -> str:
        """Return the next note filename and advance the sequence."""
        song = self._songs[self._song_idx]
        note = song.notes[self._note_idx]
        self._note_idx += 1
        if self._note_idx >= len(song.notes):
            self._note_idx = 0
            self._song_idx = (self._song_idx + 1) % len(self._songs)
        return note

    @property
    def current_song(self) -> str:
        """Name of the song the next() call will draw its note from."""
        return self._songs[self._song_idx].name
